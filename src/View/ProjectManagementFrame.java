    public void loadProjectsIntoTable(List<ProjectBean> list) {
    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
    dtm.setRowCount(0);

    for (ProjectBean project : list) {
        Vector<Object> v = new Vector<>();
        v.add(project.getProjectId());
        v.add(project.getProjectName());
        v.add(project.getProjectDescription());
        v.add(project.getStartDate());
        v.add(project.getEndDate());
        v.add(project.getStatus());
        dtm.addRow(v);
    }
}
    public void loadTable(){
        //access those method which selecting all data
        
        ProjectBal bal = new ProjectBal();
        List<ProjectBean> list = bal.loadProjects();
        loadProjectsIntoTable(list);
    }
    
