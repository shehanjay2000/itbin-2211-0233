    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       int row = jTable1.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a project to update or delete.");
        } else {
            projectId = (int) jTable1.getValueAt(row, 0);
            ProjectBal bal = new ProjectBal();
            ProjectBean project = bal.loadProjectById(projectId);

            jTextFieldProjectName.setText(project.getProjectName());
            jTextArea1.setText(project.getProjectDescription());
            jDateChooserStartDate.setDate(project.getStartDate());
            jDateChooserEndDate.setDate(project.getEndDate());
            jComboBoxStatus.setSelectedItem(project.getStatus());
        }
    }//GEN-LAST:event_jTable1MouseClicked

